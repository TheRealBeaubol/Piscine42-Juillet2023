int	len_tab(char **tab)
{
	int	i;
	int	n;
	int	j;

	i = 0;
	n = 0;
	while (tab[i] != 0)
	{
		j = 0;
		while (tab[i][j] != 0)
		{
			j++;
			n++;
		}
		i++;
	}
	return (n);
}
